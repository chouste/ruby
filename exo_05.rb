# le code # permet d'effectuer une action au milieu d'une valeur string

puts "On va comptere les nombres d'heure de travail à THP" # la phrase apparrait
puts "Travail : #{10 * 5 * 11}"# on apperçoit la phase et le résultat du calcul
puts "En minutes ça fait : #{10 * 5 * 11 * 60}"# on apperçoit la phase et le résultat du calcul

puts " Et en secondes ?"# la phrase apparrait

puts 10 * 5 * 11 * 60 * 60# on obtient le resultat du calcul

puts "Est- ce que c'est vrai que 3 + 2 < 5 - 7 ?" #la phrase apparait ainsi que le calcul

puts 3 + 2 < 5 - 7 #le bouléen false apparait

puts "ça fait combien 3 + 2 ? #{3+2}"#la phrase apparait ainsi que le resultat du calcul
puts "ça fait combien 5 - 7 ? #{5-7}"#la phrase apparait ainsi que le resultat du calcul

puts "Ok, c'est faux alors !"#la phrase apparait 

puts " C'est drole ça, faisons-en plus:"#la phrase apparait

puts "Est ce que 5 est plus grand que -2? #{5 > -2}" #la phrase apparait ainsi que le bouleen true
puts "Est que 5 est supérieur ou égale à -2? #{5 >= -2}"#la phrase apparait ainsi que le bouleen true
